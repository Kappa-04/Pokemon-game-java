package it.unibs.pajc;

public class StatChange {
	private String stat;
	private int change;
	public String getStat() {
		return stat;
	}
	public int getChange() {
		return change;
	}
	public StatChange(String stat, int change) {
		super();
		this.stat = stat;
		this.change = change;
	}
	
	
}
